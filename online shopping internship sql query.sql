-- 2. Find the average number of products per order, grouped by customer city. 


SELECT c.customer_city,CAST(COUNT(a.product_id)*1.0 
        / COUNT(DISTINCT o.order_id)AS DECIMAL(10,1)
    ) AS average_products_per_order
FROM orders o
JOIN customers c ON o.customer_id = c.customer_id
JOIN order_items a ON o.order_id = a.order_id
GROUP BY c.customer_city
ORDER BY average_products_per_order DESC;


