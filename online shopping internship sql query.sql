--5. Calculate the retention rate of customers, defined as the percentage of customers 
--who make another purchase within 6 months of their first purchase. 

WITH first_purchase AS (SELECT customer_id,
MIN(order_purchase_timestamp) AS first_purchase_date
FROM orders
GROUP BY customer_id),
merged AS (SELECT 
o.customer_id,
o.order_purchase_timestamp,
fp.first_purchase_date,
DATEDIFF(DAY, fp.first_purchase_date, o.order_purchase_timestamp) AS date_diff
FROM orders o
JOIN first_purchase fp 
ON o.customer_id = fp.customer_id),
repeat_customers AS (SELECT DISTINCT customer_id
FROM merged
WHERE date_diff > 0 
AND date_diff <= 180)
SELECT COUNT(*) * 100.0 / 
(SELECT COUNT(*) FROM first_purchase) AS retained_rate
FROM repeat_customers;





 




