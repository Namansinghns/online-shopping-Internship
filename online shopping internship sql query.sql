--5. . Identify the correlation between product price and the number of times a product has been purchased.

WITH product_data AS (SELECT product_id,
CAST(COUNT(order_id) AS FLOAT) AS time_purchased,
CAST(AVG(price) AS FLOAT) AS price
FROM order_items
GROUP BY product_id)
SELECT(AVG(price * time_purchased) - AVG(price) * AVG(time_purchased))
 /NULLIF(STDEV(price) * STDEV(time_purchased), 0) AS correlation
FROM product_data
WHERE price IS NOT NULL 
AND time_purchased IS NOT NULL;
 





-- 1. Calculate the moving average of order values for each customer over their order history.

select a.customer_id, a.order_id ,a.order_purchase_timestamp,
       round(sum(b.price + b.freight_value),2) as order_value,
       cast(avg(sum(b.price + b.freight_value)) 
       over(partition by a.customer_id 
       order by a.order_purchase_timestamp
       rows between unbounded preceding and current row) as decimal(10,2)) as moving_avg
from orders a
join order_items b on a.order_id = b.order_id
group by a.customer_id , a.order_id , a.order_purchase_timestamp;




