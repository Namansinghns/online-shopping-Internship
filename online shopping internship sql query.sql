 -- 3. Calculate the year-over-year growth rate of total sales



 WITH yearly_sales AS (SELECT 
    YEAR(y.order_purchase_timestamp) AS year,
    ROUND(SUM(x.price),2) AS year_total
    FROM orders y
    JOIN order_items x ON y.order_id = x.order_id
    GROUP BY YEAR(y.order_purchase_timestamp))
SELECT year,year_total,
    LAG(year_total) OVER (ORDER BY year) AS previous_year_sales,
    ROUND(((year_total - LAG(year_total) OVER (ORDER BY year))
        / LAG(year_total) OVER (ORDER BY year)) * 100,2) AS yoy_growth_rate
FROM yearly_sales;




